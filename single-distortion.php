						<p>Is U.S. President Donald Trump going to fire Special Counsel Robert Mueller? That’s what Rep. Jackie Speier (D-San Mateo) speculated on Friday and what The New York Times and The Washington Post suggested in their coverage of a letter from Trump’s lawyer. Specifically, these outlets imply the letter, which said Mueller improperly obtained emails belonging to Trump’s transition team, was an indication of the president’s efforts to interfere with the investigation—whether by firing Mueller or simply discrediting him.</p>
						<p>As we’ll show, The Times and The Post gave few facts to support this speculation and the information they did provide was obscured by subjective, vague language – a.k.a spin. Because the spin and lack of information makes it difficult to evaluate the merit of the speculation (Trump is going to fire Mueller), some readers may not question it and instead accept it as fact, or at least as likely. However, with a few questions that we explore below, you can start to evaluate the speculation for yourself.</p>
						<p>First, let’s look at two sentences that support the rumor that Trump is going to fire Mueller (note the spin words highlighted in red).</p>
						<blockquote>
							<p>“The <strong>growing criticism</strong> of Mr. Mueller has <a href="">fueled</a> speculation that Mr. Trump may fire him.”</p>
							<p class="source"><a href=""><img src="<?php echo $asset_path; ?>/images/fpo--logo-nyt.png" alt="Publication Name"></a></p>
						</blockquote>
						<p>Note that it’s not necessarily a problem that The Times reports this speculation – after all, some politicians, like Speier, have said they think this might happen. The issue is more how the outlet reports the rumor; it uses spin to frame the letter as part of a trend of “growing criticism” that warrant.</p>
						<blockquote>
							<p>“The letter from Langhofer is the <strong>latest</strong> in a series of legal and public relations moves by Trump’s allies to attempt to undermine Mueller’s investigation and portray it as politically motivated.”
							<p class="source"><a href=""><img src="<?php echo $asset_path; ?>/images/fpo--logo-nyt.png" alt="Publication Name"></a></p>
						</blockquote>
						<p>Here, The Post doesn’t directly suggest Trump will fire Mueller, but does suggest he’s trying to “undermine” due process.</p>
						<p>Trump may end up firing Mueller and it’s possible he’s attempting to subvert due process. Yet even so, it’s not responsible reporting for media outlets to imply this is Trump’s intent without providing supporting evidence.</p>
						<h2>How to evaluate speculation</h2>
						<p>The following are a few questions you can ask yourself when evaluating speculation in the media:</p>
						<ol>
							<li>
								<p>On what data is the speculation based?</p>
								<p>Speculation usually doesn’t come out of nowhere. It’s often an inference based on a number of facts. What are the facts in this case? That’s part of the issue – The Post and The Times provide few. Here are some facts which The Times and The Post did not include:</p>
								<p>While the two articles said or implied that Trump or his supporters have criticized the Russia investigation, they didn’t include what was specifically said. For example, in a tweet Trump states, “Despite thousands of hours wasted and many millions of dollars spent, the Democrats have been unable to show any collusion with Russia.” While he’s directly criticizing Democrats, he’s indirectly riticizing efforts to investigate possible “collusion” with Russia. Trump has also called the investigation the “Russia Witch Hunt” and “the greatest Witch Hunt in U.S. political history,” presumably referring to Mueller’s investigation, though he doesn’t say this explicitly.</p>
								<p>Rep. Jackie Speier said on Friday, “The rumor on the Hill when I left yesterday was that the president was going to make a significant speech at the end of next week. And on Dec. 22, when we are out of D.C., he was going to fire Robert Mueller.”</p>
								<p>When Trump was asked Sunday evening if he was thinking of firing Mueller, he said, “No, I’m not.”</p>
								<p>This is by no means a comprehensive list of facts. These are just a few bits of information one might examine when evaluating the likelihood of Trump firing Mueller. More facts would be required for a more robust evaluation.</p>
								<p>But let’s say you have collected more facts; here are some next steps.</p>
							</li>
							<li>
								<p>Are there other explanations? How do the different explanations compare?</p>
								<p>In his book “A Field Guide to Lies, Critical Thinking in the Information Age,” Daniel J. Levitin says, “When evaluating a claim or argument, ask yourself  if there is another reason – other than the one offered – that could account for the facts or observations that have been reported.”</p>
								<p>In this case, the explanation offered is that Trump might fire Mueller. It’s difficult to come up with other possibilities without knowing what facts the original speculation was based on. But if we look at the above facts, other explanations could be:</p>
								<ul>
									<li>Trump may try to fire Mueller, and there may be a legal basis for doing so.</li>
									<li>The rumor may be just that – a rumor, with minimal or no facts to substantiate it.</li>
									<li>Trump’s criticism of the investigation may be an attempt to put pressure on Mueller to arrive at a favorable outcome, rather than an indication he’s going to fire him.</li>
									<li>Once we come up with alternate explanations, we then need to weigh them against each other and decide which one is more likely. But when doing so, beware of bias and common errors in reasoning. If you found yourself immediately dismissive, or felt uncomfortable in response to some of the above possibilities, it may be an indication of bias. The next step could help.</li>
								</ul>
							</li>
							<li>
								<p>Are any biases clouding your evaluation?</p>
								<p>Humans are fallible, but being aware of common errors in reasoning, such as cognitive biases and logical fallacies, can help. Here are two cognitive biases that might influence your evaluation in the previous step:</p>
								<p>Availability Heuristic: School of Thought founder Jesse Richardson summarizes this bias as, “Your judgements are influenced by what springs most easily to mind.” In this case, if it’s easier to imagine Trump firing Mueller than not, then you may overestimate the likelihood of the former.</p>
							</li>
